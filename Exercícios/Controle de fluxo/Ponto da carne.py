temperatura = int(input('Qual a temperatura da carne?'))

if temperatura == 48:
    print('Selada')
elif temperatura == 54:
    print('Ao ponto para mal')
elif temperatura == 60:
    print('Ao ponto')
elif temperatura == 65:
    print('Ao ponto para o bem')
elif temperatura == 71:
    print('Bem passada')                
